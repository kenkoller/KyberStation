/**
 * Extended Star Wars location and faction theme palettes.
 *
 * Builds on the base CanvasTheme interface from canvasThemes.ts, adding
 * material properties, ambient effects metadata, category tagging,
 * and descriptive text for each theme.
 *
 * Color format conventions match canvasThemes.ts exactly:
 *   - Space-separated RGB channels for Tailwind opacity modifier colors
 *   - Full rgba strings for baked-alpha colors (accentDim, borders)
 *   - Hex strings for Canvas2D API colors
 *   - Comma-separated RGB for vignetteColor
 */

import type { CanvasTheme } from './canvasThemes';

export interface ExtendedCanvasTheme extends CanvasTheme {
  material: {
    surfaceStyle: 'matte' | 'satin' | 'gloss';
    panelOpacity: number;
    borderStyle: 'subtle' | 'lined' | 'glow';
    cornerStyle: 'rounded' | 'angular' | 'clipped';
  };
  ambient: {
    particleDensity: number;
    particleColor: string;
    scanSweep: boolean;
    scanColor: string;
    consoleBlinkRate: number;
    gridAnimated: boolean;
    hudStyle: 'imperial' | 'rebel' | 'republic' | 'minimal';
  };
  category: 'location' | 'faction';
  description: string;
}

